int		ft_iterative_power(int nb, int power)
{
	int i;
	int finalnumber;

	i = 0;
	finalnumber = 1;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else
	{
		while (i < power)
		{
			i++;
			finalnumber = nb * finalnumber;
		}
	}
	return (finalnumber);
}
